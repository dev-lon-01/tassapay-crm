const fs = require("fs");

const route = [
  `import { NextRequest, NextResponse } from "next/server";`,
  `import { pool } from "@/src/lib/db";`,
  `import { requireAuth } from "@/src/lib/auth";`,
  `import type { RowDataPacket } from "mysql2";`,
  ``,
  `/**`,
  ` * GET /api/tasks`,
  ` *`,
  ` * Query params (all optional):`,
  ` *   ?queueType=  default | dormant | new | incomplete  (default: "default")`,
  ` *   ?timeframe=  number of days for dormant/new modes`,
  ` *   ?search=     full-text filter on name, phone, id`,
  ` *   ?country=    exact country match`,
  ` */`,
  `export async function GET(req: NextRequest) {`,
  `  const auth = requireAuth(req);`,
  `  if (auth instanceof NextResponse) return auth;`,
  ``,
  `  try {`,
  `    const { searchParams } = new URL(req.url);`,
  `    const queueType = searchParams.get("queueType")?.trim() || "default";`,
  `    const timeframe = parseInt(searchParams.get("timeframe") ?? "0", 10);`,
  `    const search    = searchParams.get("search")?.trim()  ?? "";`,
  `    const country   = searchParams.get("country")?.trim() ?? "";`,
  ``,
  `    const params: (string | number)[] = [];`,
  ``,
  `    // \u2500\u2500 queue-type WHERE clause`,
  `    let baseWhere: string;`,
  ``,
  `    switch (queueType) {`,
  `      case "dormant": {`,
  `        const days = timeframe > 0 ? timeframe : 40;`,
  `        baseWhere = \`(`,
  `          c.total_transfers > 0`,
  `          AND (`,
  `            SELECT MAX(t.created_at)`,
  `            FROM   transfers t`,
  `            WHERE  t.customer_id = c.customer_id`,
  `          ) <= DATE_SUB(NOW(), INTERVAL ? DAY)`,
  `        )\`;`,
  `        params.push(days);`,
  `        break;`,
  `      }`,
  `      case "new": {`,
  `        const days = timeframe > 0 ? timeframe : 7;`,
  `        baseWhere = \`c.registration_date >= DATE_SUB(NOW(), INTERVAL ? DAY)\`;`,
  `        params.push(days);`,
  `        break;`,
  `      }`,
  `      case "incomplete":`,
  `        baseWhere = \`c.kyc_completion_date IS NULL\`;`,
  `        break;`,
  `      default:`,
  `        baseWhere = \`(`,
  `          c.kyc_completion_date IS NULL`,
  `          OR (`,
  `            c.total_transfers = 0`,
  `            AND c.registration_date >= DATE_SUB(NOW(), INTERVAL 30 DAY)`,
  `          )`,
  `        )\`;`,
  `        break;`,
  `    }`,
  ``,
  `    // \u2500\u2500 optional filters`,
  `    let extraWhere = "";`,
  ``,
  `    if (search) {`,
  "      const like = `%${search}%`;",
  `      extraWhere += \` AND (c.full_name LIKE ? OR c.phone_number LIKE ? OR c.customer_id LIKE ?)\`;`,
  `      params.push(like, like, like);`,
  `    }`,
  `    if (country) {`,
  `      extraWhere += \` AND c.country = ?\`;`,
  `      params.push(country);`,
  `    }`,
  ``,
  "    const sql = `",
  `      SELECT`,
  `        c.customer_id,`,
  `        c.full_name,`,
  `        c.email,`,
  `        c.phone_number,`,
  `        c.country,`,
  `        c.registration_date,`,
  `        c.kyc_completion_date,`,
  `        c.risk_status,`,
  `        c.total_transfers,`,
  `        (`,
  `          SELECT MAX(t.created_at)`,
  `          FROM   transfers t`,
  `          WHERE  t.customer_id = c.customer_id`,
  `        ) AS last_transfer_date`,
  `      FROM customers c`,
  "      WHERE ${baseWhere}${extraWhere}",
  `      ORDER BY`,
  `        CASE`,
  `          WHEN c.kyc_completion_date IS NULL THEN 0`,
  `          WHEN c.total_transfers = 0         THEN 1`,
  `          ELSE                                    2`,
  `        END ASC,`,
  `        c.registration_date ASC`,
  `      LIMIT 500`,
  "    `;",
  ``,
  `    const [rows] = await pool.execute<RowDataPacket[]>(sql, params);`,
  `    return NextResponse.json(rows);`,
  `  } catch (err) {`,
  `    const message = err instanceof Error ? err.message : String(err);`,
  `    console.error("[GET /api/tasks]", message);`,
  `    return NextResponse.json({ error: message }, { status: 500 });`,
  `  }`,
  `}`,
].join("\n");

fs.writeFileSync("app/api/tasks/route.ts", route, "utf8");
console.log("route.ts written:", route.length, "bytes");
